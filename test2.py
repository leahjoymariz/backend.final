# working with list


# create a list
ages = []


# add element to a list
ages.append(38)
ages.append(85)
ages.append(80)
ages.append(95)
ages.append(90)
ages.append(98)


print(ages)



# for loop
total = 0
for age in ages:
    total = total + age

# print the sum of all ages
print(total)